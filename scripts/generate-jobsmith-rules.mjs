// Generates packages/mcp-server/src/jobsmith-rules.generated.ts from JobSmith's
// real rule pack (apps/jobsmith/rules/jobsmith-universal-rules-v1.yaml), so the
// MCP connector runs the SAME rules as the JobSmith app without a Vite-only
// `?raw` import. Emits a .ts module (repo convention for generated data).
//
// Run: node scripts/generate-jobsmith-rules.mjs

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { load as parseYaml } from "js-yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const SRC = path.join(ROOT, "apps/jobsmith/rules/jobsmith-universal-rules-v1.yaml");
const OUT = path.join(ROOT, "packages/mcp-server/src/jobsmith-rules.generated.ts");

const pack = parseYaml(fs.readFileSync(SRC, "utf8"));

const rules = (pack.rules ?? [])
  .filter((r) => r.notes !== "personal_override")
  .map((r) => ({
    rule_id: String(r.rule_id),
    name: String(r.name ?? ""),
    category: String(r.category ?? ""),
    what: String(r.what ?? ""),
    severity: String(r.severity ?? "INFO"),
    decay_period_days: r.decay_period_days ?? null,
    last_verified_at: r.last_verified_at ?? null,
    check_method: {
      type: String(r.check_method?.type ?? "human_review"),
      spec: String(r.check_method?.spec ?? ""),
    },
  }));

const data = {
  version: Number(pack.version ?? 1),
  total_rules: rules.length,
  rules,
};

const ts = `// AUTO-GENERATED by scripts/generate-jobsmith-rules.mjs - do not edit by hand.
// Source: apps/jobsmith/rules/jobsmith-universal-rules-v1.yaml

export type JobsmithRuleSeverity = "ERROR" | "WARN" | "INFO";

export interface JobsmithRawRule {
  rule_id: string;
  name: string;
  category: string;
  what: string;
  severity: JobsmithRuleSeverity;
  decay_period_days: number | null;
  last_verified_at: string | null;
  check_method: { type: string; spec: string };
}

export interface JobsmithRulePack {
  version: number;
  total_rules: number;
  rules: JobsmithRawRule[];
}

export const JOBSMITH_RULE_PACK: JobsmithRulePack = ${JSON.stringify(data, null, 2)};
`;

fs.writeFileSync(OUT, ts);
console.log(`Wrote ${path.relative(ROOT, OUT)} (${rules.length} rules, version ${data.version}).`);
