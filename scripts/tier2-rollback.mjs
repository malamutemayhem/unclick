#!/usr/bin/env node

/**
 * Tier-2 one-step rollback (the third safety leg for safe-tier auto-merge).
 *
 * Auto-merge (scripts/tier2-auto-merge-queue-check.mjs) squash-merges a
 * low-risk PR into the default branch. That gives us two safety legs already:
 *   1. Risk scoring + gate checks BEFORE the merge.
 *   2. TIER2_AUTOMERGE_EXECUTE=false dry-run default.
 *
 * This file adds the third leg: a single, reversible command to undo a
 * merge that should not have landed. Because auto-merge uses a squash
 * merge, every merged PR maps to exactly one commit on the base branch
 * (the squash commit), so the rollback is a plain `git revert` of that one
 * commit, opened as its own PR. A revert PR is itself reversible (it can be
 * reverted again), so this never destroys history and never force-pushes.
 *
 * Safety properties:
 *   - Dry-run by default (TIER2_ROLLBACK_EXECUTE=false). Prints the plan only.
 *   - Operates on ONE explicitly named PR number. No queue, no fan-out.
 *   - Refuses PRs that are not MERGED or have no squash commit to revert.
 *   - Refuses merges older than TIER2_ROLLBACK_MAX_AGE_HOURS (default 72h)
 *     unless TIER2_ROLLBACK_ALLOW_OLD=true, so a stale request cannot quietly
 *     revert a commit that later work was built on top of.
 *   - Never force-pushes, never deletes branches, never hard-resets.
 *   - Redacted: no tokens or secrets are read, printed, or logged here.
 *
 * Usage:
 *   node scripts/tier2-rollback.mjs --pr 1234              # dry-run plan
 *   TIER2_ROLLBACK_EXECUTE=true node scripts/tier2-rollback.mjs --pr 1234
 */

import { spawn } from "node:child_process";

const DEFAULT_REPO = "malamutemayhem/unclick";

export function parseBoolean(value, fallback = false) {
  if (value === undefined || value === null || value === "") return fallback;
  return /^(1|true|yes|y|on)$/i.test(String(value).trim());
}

export function parseBoundedInt(value, fallback, min, max) {
  const parsed = Number.parseInt(String(value ?? ""), 10);
  if (!Number.isFinite(parsed)) return fallback;
  return Math.max(min, Math.min(max, parsed));
}

export function compact(value, max = 200) {
  const text = String(value ?? "").replace(/\s+/g, " ").trim();
  return text.length > max ? `${text.slice(0, max - 3)}...` : text;
}

export function prNumber(value) {
  const parsed = Number.parseInt(String(value ?? ""), 10);
  return Number.isFinite(parsed) && parsed > 0 ? parsed : null;
}

function normalizeState(value) {
  return String(value ?? "").trim().toUpperCase() || "UNKNOWN";
}

function shortSha(value) {
  return String(value ?? "").trim().slice(0, 12);
}

function hoursBetween(fromIso, toIso) {
  const from = Date.parse(String(fromIso ?? ""));
  const to = Date.parse(String(toIso ?? ""));
  if (!Number.isFinite(from) || !Number.isFinite(to)) return null;
  return (to - from) / (1000 * 60 * 60);
}

/**
 * Pure planner. Given the PR detail and the run options, decide whether the
 * merge can be one-step reverted and produce the exact branch name and the
 * ordered command plan. No side effects, fully unit-testable.
 */
export function planTier2Rollback({
  pr = {},
  now = new Date().toISOString(),
  maxAgeHours = 72,
  allowOld = false,
  repo = DEFAULT_REPO,
} = {}) {
  const number = prNumber(pr.number);
  const reasons = [];

  if (!number) {
    return { ok: false, eligible: false, reason: "missing_pr_number", reasons: ["missing pr number"] };
  }

  const state = normalizeState(pr.state);
  if (state !== "MERGED") {
    reasons.push(`pr state is ${state}, not MERGED`);
    return { ok: true, eligible: false, number, reason: "not_merged", reasons };
  }

  const mergeOid =
    (pr.mergeCommit && (pr.mergeCommit.oid || pr.mergeCommit.sha)) || pr.mergeCommitOid || "";
  if (!shortSha(mergeOid)) {
    reasons.push("no squash/merge commit recorded on the PR");
    return { ok: true, eligible: false, number, reason: "no_merge_commit", reasons };
  }

  const baseRef = String(pr.baseRefName || pr.base?.ref || "main").trim() || "main";

  const ageHours = hoursBetween(pr.mergedAt, now);
  if (ageHours !== null && ageHours > maxAgeHours && !allowOld) {
    reasons.push(
      `merge is ${Math.round(ageHours)}h old (> ${maxAgeHours}h); pass --allow-old to override`,
    );
    return { ok: true, eligible: false, number, reason: "merge_too_old", age_hours: Math.round(ageHours), reasons };
  }

  const branch = `revert/pr-${number}-${shortSha(mergeOid)}`;
  const title = compact(`Revert PR #${number}: ${pr.title ?? ""}`.trim(), 120);
  const body = [
    `One-step rollback of safe-tier auto-merged PR #${number}.`,
    "",
    `Reverts squash commit ${shortSha(mergeOid)} on ${baseRef}.`,
    "Generated by scripts/tier2-rollback.mjs (third safety leg).",
    "This revert is itself reversible: revert this PR to restore the change.",
  ].join("\n");

  // Ordered, side-effect-free description of what execution will run. No
  // force-push, no branch deletion, no hard reset anywhere in the plan.
  const commands = [
    ["git", ["fetch", "origin", baseRef]],
    ["git", ["checkout", "-B", branch, `origin/${baseRef}`]],
    ["git", ["revert", "--no-edit", shortSha(mergeOid)]],
    ["git", ["push", "origin", branch]],
    [
      "gh",
      [
        "pr",
        "create",
        "--repo",
        repo,
        "--base",
        baseRef,
        "--head",
        branch,
        "--title",
        title,
        "--body",
        body,
        "--draft",
      ],
    ],
  ];

  reasons.push(`merged commit ${shortSha(mergeOid)} on ${baseRef} is revertable`);
  return {
    ok: true,
    eligible: true,
    number,
    base_ref: baseRef,
    merge_commit: shortSha(mergeOid),
    revert_branch: branch,
    revert_title: title,
    age_hours: ageHours === null ? null : Math.round(ageHours),
    commands,
    reasons,
  };
}

// ── gh / git runners (injectable for tests) ─────────────────────────────────

export async function runCommandJson(command, args, { cwd = process.cwd(), env = process.env } = {}) {
  return new Promise((resolve) => {
    const child = spawn(command, args, { cwd, env, shell: false, windowsHide: true });
    let stdout = "";
    let stderr = "";
    child.stdout?.on("data", (chunk) => { stdout += chunk.toString(); });
    child.stderr?.on("data", (chunk) => { stderr += chunk.toString(); });
    child.on("error", (error) => {
      resolve({ ok: false, exit_code: null, reason: "command_failed", output: compact(error.message, 500) });
    });
    child.on("close", (code) => {
      if (code !== 0) {
        resolve({ ok: false, exit_code: code, reason: "command_failed", output: compact(stderr || stdout, 500) });
        return;
      }
      try {
        resolve({ ok: true, value: JSON.parse(stdout || "{}") });
      } catch {
        resolve({ ok: false, exit_code: code, reason: "invalid_json", output: compact(stdout, 500) });
      }
    });
  });
}

export async function runCommandText(command, args, { cwd = process.cwd(), env = process.env } = {}) {
  return new Promise((resolve) => {
    const child = spawn(command, args, { cwd, env, shell: false, windowsHide: true });
    let stdout = "";
    let stderr = "";
    child.stdout?.on("data", (chunk) => { stdout += chunk.toString(); });
    child.stderr?.on("data", (chunk) => { stderr += chunk.toString(); });
    child.on("error", (error) => {
      resolve({ ok: false, exit_code: null, reason: "command_failed", output: compact(error.message, 500) });
    });
    child.on("close", (code) => {
      resolve({
        ok: code === 0,
        exit_code: code,
        reason: code === 0 ? "ok" : "command_failed",
        output: compact(stderr || stdout, 500),
      });
    });
  });
}

export async function fetchMergedPullRequest({
  repo = DEFAULT_REPO,
  number,
  cwd = process.cwd(),
  runJson = runCommandJson,
} = {}) {
  if (!prNumber(number)) return { ok: false, reason: "missing_pr_number" };
  const result = await runJson(
    "gh",
    [
      "pr",
      "view",
      String(number),
      "--repo",
      repo,
      "--json",
      "number,state,title,url,mergedAt,mergeCommit,headRefName,baseRefName",
    ],
    { cwd },
  );
  if (!result.ok) return result;
  return { ok: true, pr: result.value && typeof result.value === "object" ? result.value : {} };
}

/**
 * Orchestrator. Fetches the PR, plans the rollback, and either prints the
 * plan (dry-run, the default) or executes the ordered command list, stopping
 * at the first failure so a partial revert never silently half-applies.
 */
export async function runTier2Rollback(options = {}) {
  const repo = options.repo || process.env.GITHUB_REPOSITORY || DEFAULT_REPO;
  const number = prNumber(options.number ?? options.pr);
  const execute =
    typeof options.execute === "boolean"
      ? options.execute
      : parseBoolean(process.env.TIER2_ROLLBACK_EXECUTE, false);
  const allowOld =
    typeof options.allowOld === "boolean"
      ? options.allowOld
      : parseBoolean(process.env.TIER2_ROLLBACK_ALLOW_OLD, false);
  const maxAgeHours =
    typeof options.maxAgeHours === "number"
      ? options.maxAgeHours
      : parseBoundedInt(process.env.TIER2_ROLLBACK_MAX_AGE_HOURS, 72, 1, 720);

  if (!number) {
    return { ok: false, action: "tier2_rollback", result: "blocker", reason: "missing_pr_number", execute };
  }

  const fetchPr = options.fetchPr || fetchMergedPullRequest;
  const fetched = await fetchPr({ repo, number, cwd: options.cwd, runJson: options.runJson });
  if (!fetched.ok) {
    return {
      ok: false,
      action: "tier2_rollback",
      result: "blocker",
      reason: fetched.reason || "fetch_pr_failed",
      execute,
      number,
      output: fetched.output || "",
    };
  }

  const plan = planTier2Rollback({
    pr: fetched.pr,
    now: options.now || new Date().toISOString(),
    maxAgeHours,
    allowOld,
    repo,
  });

  if (!plan.eligible) {
    return { ...plan, ok: true, action: "tier2_rollback", result: "skipped", execute };
  }

  if (!execute) {
    return {
      ...plan,
      ok: true,
      action: "tier2_rollback",
      result: "planned",
      execute: false,
      no_execute_reason: "dry_run_default",
    };
  }

  const runText = options.runText || runCommandText;
  const cwd = options.cwd || process.cwd();
  const steps = [];
  for (const [command, args] of plan.commands) {
    const res = await runText(command, args, { cwd });
    steps.push({ command, ok: Boolean(res.ok), reason: res.reason || (res.ok ? "ok" : "failed"), output: res.ok ? "" : compact(res.output || "", 300) });
    if (!res.ok) {
      return {
        ...plan,
        ok: false,
        action: "tier2_rollback",
        result: "rollback_blocker",
        reason: "rollback_command_failed",
        execute: true,
        steps,
      };
    }
  }

  return { ...plan, ok: true, action: "tier2_rollback", result: "reverted", execute: true, steps };
}

// ── CLI ─────────────────────────────────────────────────────────────────────

function parseArgs(argv) {
  const out = {};
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === "--pr" || arg === "--number") out.number = argv[++i];
    else if (arg === "--execute") out.execute = true;
    else if (arg === "--allow-old") out.allowOld = true;
    else if (arg === "--repo") out.repo = argv[++i];
    else if (arg.startsWith("--pr=")) out.number = arg.slice("--pr=".length);
  }
  return out;
}

const isMain = (() => {
  try {
    return import.meta.url === `file://${process.argv[1]}`;
  } catch {
    return false;
  }
})();

if (isMain) {
  const args = parseArgs(process.argv.slice(2));
  runTier2Rollback(args)
    .then((result) => {
      process.stdout.write(`${JSON.stringify(result, null, 2)}\n`);
      // A blocker is a real failure; an eligible-but-skipped plan is not.
      if (result.result === "blocker" || result.result === "rollback_blocker") {
        process.exitCode = 1;
      }
    })
    .catch((error) => {
      process.stdout.write(`${JSON.stringify({ ok: false, result: "blocker", reason: "unexpected_error", output: compact(error?.message, 500) }, null, 2)}\n`);
      process.exitCode = 1;
    });
}
