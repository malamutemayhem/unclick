// generate-tool-index.mjs
// Builds a self-updating index of every tool UnClick exposes, straight from the
// source of truth (tool-wiring.ts). This is the spine for tool discovery: the
// boot capability briefing and (next) unclick_search both read from it, so
// adding a tool automatically updates what the agent knows it can do.
//
// Emits:
//   src/memory/tool-index.generated.ts   (TOOL_INDEX, shipped in the package)
//   docs/tool-index.generated.json       (human-readable view)
//
// Usage:
//   node scripts/generate-tool-index.mjs            (write the files)
//   node scripts/generate-tool-index.mjs --check    (fail if committed file is stale)

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SRC = path.resolve(__dirname, "../src");
const REPO = path.resolve(__dirname, "../../..");
const OUT_TS = path.join(SRC, "memory/tool-index.generated.ts");
const OUT_JSON = path.join(REPO, "docs/tool-index.generated.json");

/** Parse tool-wiring.ts into [{ app, category, tools: [{ name, description }] }]. */
export function buildToolIndex() {
  const wiring = fs.readFileSync(path.join(SRC, "tool-wiring.ts"), "utf8");

  // 1) slug -> category, from the import-region section headers.
  const importRegion = wiring.slice(0, wiring.indexOf("export const ADDITIONAL_TOOLS"));
  const slugCategory = {};
  let category = "Other";
  for (const ln of importRegion.split("\n")) {
    const h = ln.match(/^\/\/\s*[─-]{3,}\s*(.+?)\s*[─-]{3,}\s*$/);
    if (h) { category = h[1].trim(); continue; }
    const im = ln.match(/from\s+"\.\/([a-z0-9-]+)-tool\.js"/);
    if (im && !(im[1] in slugCategory)) slugCategory[im[1]] = category;
  }

  // 2) slug -> tools, from the ADDITIONAL_TOOLS blocks.
  const start = wiring.indexOf("export const ADDITIONAL_TOOLS");
  const body = wiring.slice(wiring.indexOf("[", start) + 1, wiring.indexOf("\n];", start));
  const headerRe = /\/\/\s*[─-]+\s*([a-z0-9-]+)-tool\.ts\s*[─-]*/g;
  const headers = [...body.matchAll(headerRe)];
  const index = [];
  for (let i = 0; i < headers.length; i++) {
    const slug = headers[i][1];
    const from = headers[i].index + headers[i][0].length;
    const to = i + 1 < headers.length ? headers[i + 1].index : body.length;
    const chunk = body.slice(from, to);
    const tools = [...chunk.matchAll(/name:\s*"([^"]+)",\s+description:\s*"((?:[^"\\]|\\.)*)"/g)]
      .map((m) => ({ name: m[1], description: m[2].replace(/\\"/g, '"') }));
    if (tools.length === 0) continue;
    index.push({ app: slug, category: slugCategory[slug] ?? "Other", tools });
  }
  index.sort((a, b) => a.app.localeCompare(b.app));
  return index;
}

function render(index) {
  return `// AUTO-GENERATED by scripts/generate-tool-index.mjs. Do not edit by hand.
// Run \`node scripts/generate-tool-index.mjs\` after changing tool-wiring.ts.

export interface IndexedTool {
  name: string;
  description: string;
}

export interface ToolIndexEntry {
  app: string;
  category: string;
  tools: IndexedTool[];
}

export const TOOL_INDEX: ToolIndexEntry[] = ${JSON.stringify(index, null, 2)};
`;
}

function main() {
  const index = buildToolIndex();
  const ts = render(index);
  const json = JSON.stringify(index, null, 2) + "\n";

  if (process.argv.includes("--check")) {
    const cur = fs.existsSync(OUT_TS) ? fs.readFileSync(OUT_TS, "utf8") : "";
    if (cur.trim() !== ts.trim()) {
      console.error("tool-index is stale. Run: node scripts/generate-tool-index.mjs");
      process.exit(1);
    }
    console.log(`tool-index up to date (${index.length} apps, ${index.reduce((n, e) => n + e.tools.length, 0)} tools).`);
    return;
  }

  fs.writeFileSync(OUT_TS, ts);
  fs.mkdirSync(path.dirname(OUT_JSON), { recursive: true });
  fs.writeFileSync(OUT_JSON, json);
  const toolCount = index.reduce((n, e) => n + e.tools.length, 0);
  console.log(`Wrote ${path.relative(REPO, OUT_TS)} and ${path.relative(REPO, OUT_JSON)}`);
  console.log(`${index.length} apps, ${toolCount} tools indexed.`);
}

if (process.argv[1] && pathToFileURL(path.resolve(process.argv[1])).href === import.meta.url) main();
